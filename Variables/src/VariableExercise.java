public class VariableExercise {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);
        int y = 15;
        int result = y + 10;
        System.out.println(y);
        System.out.println(result);

        int a = 18;
        int b = 10;
        result = a / b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(result);

        int c = 12;
        System.out.println(c);
        System.out.println(c);
        System.out.println(c);
        System.out.println(c);
        System.out.println(c);
    }
}
